class Solution {
    public int[] searchRange(int[] nums, int target) {
        int len = nums.length;
        if (len == 0) return new int[] {-1, -1};

        int l = 0, r = len-1;
        while (l < r) {
            int mid = l + r >> 1;
            if (nums[mid] >= target) {
                r = mid;
            } else {
                l = mid + 1;
            }
        }

        if (nums[l] != target) return new int[] {-1, -1};

        int[] res = new int[] {-1,-1};
        res[0] = l;
        r = len-1;
        while (l < r) {
            int mid = l + r + 1 >> 1;
            if (nums[mid] <= target) {
                l = mid;
            } else {
                r = mid-1;
            }
        }
        res[1] = r;
        return res;
    }
}
